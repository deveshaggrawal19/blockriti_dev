	var T_security = T.security = {
		lastByte: function (str) {
			words = Crypto.enc.Utf8.parse(str).words;
			for (var bytes = [], b = 0; b < words.length * 32; b += 8)
				bytes.push((words[b >>> 5] >>> (24 - b % 32)) & 0xFF);
			var last = 0;
			do { last = bytes.pop(); } while( last == 0 )
			return last;
		},
		getSalt: function(ref) {
			return SALTS[this.lastByte(ref)];
		},
		hash: function() {
			var message = '', tohash = Array.prototype.slice.call(arguments);
			for(v in tohash) message += tohash[v] + this.getSalt(tohash[v]);
			return Crypto.SHA256(message).toString();
		},
		check: function() {
			var args = Array.prototype.slice.call(arguments);
			return args[0] == this.hash.apply(this, args.slice(1));
		}
	};
